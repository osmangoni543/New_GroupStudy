package com.java.Practice;

public class One_Dimensional_Arrays_Revision {

	public static void main(String[] args) {
		
		/*
		 * int[] a;
		 * 
		 * a = new int[5];
		 * 
		 * a[0] = 1; 
		 * a[1] = 2; 
		 * a[2] = 3; 
		 * a[3] = 4; 
		 * a[4] = 5;
		 */
		         //0,1,2,3,4
		int[] a = {1,2,3,4,5};
		
		System.out.println(a.length);
		System.out.println(a[3]);
		
		
		for(int i=0; i<a.length; i++) {
			System.out.println(a[i]);
		}
		
		
	}

}
