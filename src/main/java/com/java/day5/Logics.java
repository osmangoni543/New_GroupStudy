package com.java.day5;

public class Logics {

	public static void addition() {
		int i = 10;
		int j = 5;
		System.out.println(i+j);	
	}
	
	private static void substraction() { 
		int i = 10;
		int j = 5;
		System.out.println(i-j);	
	}
	
	static void multiplication() {
		int i = 10;
		int j = 5;
		System.out.println(i*j);	
	}
	
	protected static void division() {
		int i = 10;
		int j = 5;
		System.out.println(i/j);	
	}

}
