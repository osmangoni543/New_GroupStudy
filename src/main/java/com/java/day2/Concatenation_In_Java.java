package com.java.day2;

public class Concatenation_In_Java {

	public static void main(String[] args) {
		//Concatenation means joining and it is represented  by +

			 String S1 = "My Name is:";

			 String S2 = "Ali";

			 System.out.println(S1 + " " +S2); // My Name is: Ali

			 String S3 = "My Language is:";

			 String S4 = "English";

			 System.out.println(S3 + " " +S4); // My Language is: English

			 String S5 = "My Country is:";

			 String S6 = "Bangladesh";

			 System.out.println(S5 + " " + S6); //My country is: Bangladesh

			 

			 

			 int i = 10;

			 int j = 20;

			 String S7 = "Hello";

			 String S8 = "World";

			 System.out.println(i+j+S7+S8);

			 System.out.println(S7+S8+i+j);

			 System.out.println(S7+S8+(i+j)) ;

		}


	}


